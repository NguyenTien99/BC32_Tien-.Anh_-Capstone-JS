
function Product(id,name,price,screen, frontCamera, backCamera,img,type, desc){
    this.id = id;
    this.name = name;
    this.price = price;
    this.screen = screen;
    this.img = img;
    this.backCamera = backCamera;
    this.frontCamera = frontCamera;
    this.desc = desc;
    this.type = type;
}